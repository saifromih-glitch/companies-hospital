def is_deep_question(message: str) -> bool:
    """Check if this question needs deep methodology"""
    text = message.lower()
    # Check if multi-part or long question
    if len(message) > 150:
        return True
    if "؟" in message and message.count("؟") >= 2:
        return True
    if "؟" in message and message.count("و") >= 4:
        return True
    # Check keywords
    return any(kw in text for kw in DEEP_KEYWORDS)

